package app;

/*
 * Exercício: Realize a implementação dos métodos para efetuar multiplicação e divisão. Por fim
 * também crie um método para calcular o delta a partir da fórmula de bhaskara.
 * 
 */

public class Calculadora {
	
	
	
	public int adicao(int x, int y)
	{
		return x + y;
	}
	
	public int subtracao(int x, int y)
	{
		return x - y;
	}

}
